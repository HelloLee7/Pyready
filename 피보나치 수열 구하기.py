# 피보나치 수열이란, 첫 번째 항의 값이 0이고 두 번째 항의 값이 1일 때, 이후의 항들은 이전의 두 항을 더한 값으로 이루어지는 수열을 말한다.

# 예) 0, 1, 1, 2, 3, 5, 8, 13

# 인풋을 정수 n으로 받았을때, n 이하까지의 피보나치 수열을 출력하는 프로그램을 작성하세요

def fibonacci_sequence(n):
    """
    n 이하의 피보나치 수열을 출력하는 함수

    Args:
        n: 정수 (최댓값)
    """
    if n < 0:  # n이 음수이면
        print("n은 0 이상의 정수여야 합니다.")  # 오류 메시지 출력
        return

    a, b = 0, 1  # 첫 번째 항과 두 번째 항 초기화
    while a <= n:  # a가 n 이하일 동안 반복
        print(a, end=" ")  # a 값 출력 (공백으로 구분)
        a, b = b, a + b  # 다음 항 계산 (a에 b를 넣고, b에 a+b를 넣음)

# 예시
n = 10
fibonacci_sequence(n)  # 출력: 0 1 1 2 3 5 8