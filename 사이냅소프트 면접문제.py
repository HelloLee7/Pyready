# 주어진 문자열(공백 없이 쉼표로 구분되어 있음)을 가지고 아래 문제에 대한 프로그램을 작성하세요.

# 이유덕,이재영,권종표,이재영,박민호,강상희,이재영,김지완,최승혁,이성연,박영서,박민호,전경헌,송정환,김재성,이유덕,전경헌

# 김씨와 이씨는 각각 몇 명 인가요?
# "이재영"이란 이름이 몇 번 반복되나요?
# 중복을 제거한 이름을 출력하세요.
# 중복을 제거한 이름을 오름차순으로 정렬하여 출력하세요.


names = "이유덕,이재영,권종표,이재영,박민호,강상희,이재영,김지완,최승혁,이성연,박영서,박민호,전경헌,송정환,김재성,이유덕,전경헌"

# 김씨와 이씨는 각각 몇 명 인가요?
kim_count = names.count("김")  # "김" 문자열이 나타나는 횟수를 세어 김씨 수 계산
lee_count = names.count("이")  # "이" 문자열이 나타나는 횟수를 세어 이씨 수 계산
print(f"김씨: {kim_count}명, 이씨: {lee_count}명")  # 결과 출력

# "이재영"이란 이름이 몇 번 반복되나요?
lee_jae_young_count = names.count("이재영")  # "이재영" 문자열이 나타나는 횟수를 세어 이재영 수 계산
print(f'"이재영" 이름 반복 횟수: {lee_jae_young_count}번')  # 결과 출력

# 중복을 제거한 이름을 출력하세요.
name_list = names.split(",")  # ","를 기준으로 문자열을 나누어 리스트 생성
unique_names = list(set(name_list))  # set을 사용하여 중복 제거 후 다시 리스트로 변환
print(f"중복 제거된 이름: {unique_names}")  # 결과 출력

# 중복을 제거한 이름을 오름차순으로 정렬하여 출력하세요.
sorted_unique_names = sorted(unique_names)  # 리스트를 오름차순으로 정렬
print(f"중복 제거 후 오름차순 정렬된 이름: {sorted_unique_names}")  # 결과 출력